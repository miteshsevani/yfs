					<p>Perfectly co-ordinated in the most beautiful, traditional, quirky punjabi suit, she walks in into the room filled with men and women of all ages and backgrounds. She’s intelligent, kind and softly spoken with an air of confidence and humility all at once. She leads a Government initiative program for the education and nourishment of pre-school children in the slums of Ahmedabad. We watch the sunset from her veranda, sip chai and talk about being an unmarried, educated woman, living in India. As we move onto politics and social injustice, I ask her if she has ever thought about moving, she replies, <blockquote>When your mother grows old and frail and looses her beauty, you don’t abandon her or find another one. In the same way, how can you say that you will leave India if she is your mother and that there is no hope?</blockquote></p>
					<p>Mad about food, with a Masters degree in Sanskrit, she teaches Niyudh (martial arts) and patiently shows me how to run the colored sand through my fingers to make a Rangoli. We dart through the lush streets of Pune on her scooter, admiring the scaled down models of Maharashtra’s forts, she narrates stories of Shivaji Maharaj to me, as I hold on tight. We talk about her family’s disapproval of her choice to dedicate her life to work full time for India’s largest grassroots organisation for women. “Mara-Sangor phool hote hai, after blossoming they naturally fall to the ground, ye swadarpan hai.” Using the flowers as an example of dedication she goes on, “if you want to dedicate your life to service or Bharat Mata do so only once you have fully blossomed, developed and grown.”</p>
					<p>Living in Ahmedabad’s largest slum area, sitting on the rugged floor with her elderly husband she waits patiently for us to deliver her daily meal. When we arrive, she quickly empties the food, washes the tiffin and returns it to me. I watch as she divides the portion barely enough for one, into three. Surprised I ask why? She smiles slowly and in her smile I see years of hardship, love and experience all at once. I am stunned by her beauty and her reply, “It is ok if we don’t eat but the dogs outside our home should not go hungry”</p>
					<p>For the first time I felt what it meant to be Indian, I experienced the beauty of our rich heritage, I connected with strangers, dined with children and danced with Ma-jis (elderly mothers).</p>
					<p>As my love for India grew, I saw the country, the people and the culture from a new perspective. My sense of identity evolved as I slowly started to understand those around me and felt responsible for the challenges India faces.</p>
